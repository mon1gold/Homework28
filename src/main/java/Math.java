public class Math {
    public static double multiply(double x, double y) {
        return x * y;
    }

    public static double sum(double x, double y) {
        return x + y;
    }

    public static double subtraction(double x, double y) {
        return x - y;
    }

    public static double divide(double x, double y) {
        return x / y;
    }
}
